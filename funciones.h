#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED

/**
* \brief Recibe dos datos numericos, realiza la suma de los mismos y devuelve el resultado.
* \param x Dato numerico con el que la funcion va a operar.
* \param y Dato numerico con el que la funcion va a operar.
* \return el resultado de la suma de dos numeros.
*
*/
float sumar(float x, float y);

/**
* \brief Recibe dos datos numericos, realiza la la resta de los mismos y devuelve el resultado.
* \param x Dato numerico con el que la funcion va a operar.
* \param y Dato numerico con el que la funcion va a operar.
* \return El resultado de la resta de dos numeros.
*
*/
float restar(float x, float y);

/**
* \brief Recibe dos datos numericos, realiza la division de los mismos y devuelve el resultado.
* \param x Dato numerico con el que la funcion va a operar.
* \param y Dato numerico con el que la funcion va a operar.
* \return El resultado de la division de dos numeros.
*
*/
float dividir(float x, float y);

/**
* \brief Recibe dos datos numericos, realiza la multiplicacion de los mismos y devuelve el resultado.
* \param x Dato numerico con el que la funcion va a operar.
* \param y Dato numerico con el que la funcion va a operar.
* \return el resultado de la multiplicacion de dos numeros.
*
*/
float multiplicar (float x, float y);

/**
* \brief Recibe un numero, calcula el factorial del mismo y devuelve el resultado.
* \param x Dato numerico con el que la funcion va a operar.
* \return El resultado de calcular el factorial de un numero.
*
*/
long long int factorial (float x);


#endif // FUNCIONES_H_INCLUDED
